    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String search1 = search.getText();
        TableRowSorter<DefaultTableModel> tr1 = new TableRowSorter<DefaultTableModel>(model);
        jTable1.setRowSorter(tr1);
        tr1.setRowFilter(RowFilter.regexFilter(search1));
    }//GEN-LAST:event_searchKeyReleased

    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        String idrl=txtmarl.getText();
        String idhs=txtmahs.getText();
        String idhk=txtmahk.getText();
        String ghichu=txtghichu.getText();
        RenLuyenDTO renLuyenDTO=new RenLuyenDTO(idrl, idhs, idhk, ghichu);
        RenLuyenBLL renLuyenBLL=new RenLuyenBLL();
        try {
            if(renLuyenBLL.AddRenluyen(renLuyenDTO)==true){
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                showdata();
                JOptionPane.showConfirmDialog(null, "thanh cong");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnaddActionPerformed

