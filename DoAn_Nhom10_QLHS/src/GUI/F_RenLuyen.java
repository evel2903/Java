    public ArrayList<RenLuyenDTO> getListRenluyen(){
        ArrayList<RenLuyenDTO> list=new ArrayList<>();
        String sql="SELECT * FROM doan.tablerenluyen";
        Statement st;
        ResultSet rs;
        
        try {
            ConnectionUtil conUtil = new ConnectionUtil();
            Connection con = conUtil.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            
            RenLuyenDTO renLuyenDTO;
            while(rs.next()){
                renLuyenDTO=new RenLuyenDTO(rs.getString("idtablerenluyen"),
                        rs.getString("idtablehocsinh"),
                        rs.getString("idtablehocki"),
                        rs.getString("ghichu")
                );
                list.add(renLuyenDTO);
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "k the hien thi");
        }
        return list;
    }
