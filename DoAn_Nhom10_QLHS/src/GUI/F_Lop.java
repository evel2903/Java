    public void showdataintable(){
        ArrayList<LopDTO> list=getListLop();
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        Object row[]=new Object[6];
        for(int i=0; i<list.size(); i++){
            row[0]=list.get(i).getIdtablelop();
            row[1]=list.get(i).getTenlop();
            row[2]=list.get(i).getSiso();
            row[3]=list.get(i).getIdtablegiaovien();
            row[4]=list.get(i).getIdtablekhoahoc();
            
            model.addRow(row);
        }
        
    }
