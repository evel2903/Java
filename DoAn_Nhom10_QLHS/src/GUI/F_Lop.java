    public ArrayList<LopDTO> getListLop(){
        
        ArrayList<LopDTO> list =new ArrayList<LopDTO>();
        String sql= "SELECT * FROM doan.tablelop";
        Statement st;
        ResultSet rs;
        
        try {
            ConnectionUtil conUtil=new ConnectionUtil();
            Connection con = conUtil.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            LopDTO lopDTO;
            
            while(rs.next()){
                lopDTO=new LopDTO(rs.getString("idtablelop"), rs.getString("tenlop"), rs.getInt("siso"), rs.getString("idtablegiaovien"), rs.getString("idtablekhoahoc"));
                list.add(lopDTO);
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "lỗi kết nối từ form");
            e.printStackTrace();
        }
        return list;
    }
    
